import UIKit

extension UIColor {
    
    static var appleBlue: UIColor {
        return UIColor(red: 0/255, green: 122/255, blue: 255/255, alpha: 1.0) /* #007aff */
    }
    
    static var incomingMessage: UIColor {
        return UIColor(red: 230 / 255, green: 230 / 255, blue: 230 / 255, alpha: 1)
    }
    
    static var defaultGreen: UIColor {
        return UIColor(red: 31/255, green: 176/255, blue: 10/255, alpha: 1.0) /* #4cd90a */
    }
    
    static var customGrey: UIColor {
        return UIColor(red: 239/255, green: 239/255, blue: 239/255, alpha: 1.0) /* #efefef */
    }
}
